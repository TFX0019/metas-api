<?php

namespace App\Models;

// use Illuminate\Contracts\Auth\MustVerifyEmail;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Relations\BelongsToMany;
use Illuminate\Database\Eloquent\Relations\HasMany;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;
use Laravel\Sanctum\HasApiTokens;

class User extends Authenticatable
{
    /** @use HasFactory<\Database\Factories\UserFactory> */
    use HasApiTokens, HasFactory, Notifiable;

    /**
     * The attributes that are mass assignable.
     *
     * @var list<string>
     */
    protected $fillable = [
        'name',
        'email',
        'password',
        'avatar',
        'puntos',
    ];

    /**
     * The attributes that should be hidden for serialization.
     *
     * @var list<string>
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    protected $casts = [
        'email_verified_at' => 'datetime',
        'password' => 'hashed',
        'puntos' => 'integer',
    ];

    public function metas(): HasMany {
        return $this->hasMany(Meta::class, 'iduser');
    }

    public function roles(): BelongsToMany {
        return $this->belongsToMany(Rol::class, 'usuario_rol', 'idusuario', 'idrol');
    }

    public function lecturas(): BelongsToMany {
        return $this->belongsToMany(Lectura::class, 'usuario_lectura', 'idusuario', 'idlectura')
        ->withTimestamps();
    }

    public function hasRole(string $role): bool {
        return $this->roles()->where('rol', $role)->exists();
    }

    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
        ];
    }
}
